from sentry.models import Agent


class AgentSerializer(serializers.Serializer):
    agent_id = serializers.CharField(max_length=64, required=True)
    public_key = serializers.RegexField(r'\A[a-zA-Z0-9+\/]+={0,2}$', max_length=50, required=True)


class AgentIndexEndpoint(Endpoint):
    authentication_classes = (SessionAuthentication, )
    permission_classes = (IsAuthenticated, )

    def get(self, request):
        """
        List your Agents
        ````````````````

        Return a list of agents know to this Sentry installation available
        to the authenticated session.

        :auth: required
        """
        queryset = Agent.objects.all()

        return self.paginate(
            request=request,
            queryset=queryset,
            order_by='agent_id',
            paginator_cls=OffsetPaginator,
            on_results=lambda x: serialize(x, request.user),
        )

